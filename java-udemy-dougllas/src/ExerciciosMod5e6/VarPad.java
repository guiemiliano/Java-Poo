package ExerciciosMod5e6;
/*
    Crie um programa Java que demonstre o valor padrão de variáveis primitivas (int,
    double, char, boolean) quando não inicializadas explicitamente.

 */

public class VarPad {
    static int i;
    static double d;
    static char c;
    static boolean b;

    public static void main(String[] args) {
        System.out.println(i);
        System.out.println(d);
        System.out.println(c);
        System.out.println(b);
    }
}
